'''
bias is a error that occurs in ml model itself due to incorrect assumptions in the ml process . 

it is the error between average model prediction and ground truth . 


Variance  is how much the model can adjust depending on the given dataset 
-refers to the changes in the model when using different portions of the training data set 


when we plot the bias variance tradeoff 

-> High bias low variance - the model is too simple and does not capture the underlying trend of the data 
-> low bias high variance - model is too sensitive and is capturing noise as if it were a real trend (overfitting)
-> High bias and high variance - model is too simple to capture the trends and is too sensitive capturing the noise as well 
-> Low bias and low variance(good and rare) - great model - captures well the underlying trends of the data and generalises well to unseen data 
 


'''

